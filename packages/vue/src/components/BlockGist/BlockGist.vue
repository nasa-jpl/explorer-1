<script setup lang="ts">
import { ref, watch } from 'vue'
import GitHub404 from './GitHub404.vue'
interface BlockGistProps {
  data: {
    blockType?: string
    caption?: string
    id?: string
    url?: string
  }
}

const props = defineProps<BlockGistProps>()
const frame = ref()
const loaded = ref(false)
const checkGistLoaded = async () => {
  if (gistUrl.value) {
    try {
      const response = await fetch(gistUrl.value)
      loaded.value = response.ok
    } catch (error) {
      loaded.value = false
    }
  }
}

const gistUrl = ref<string>('')

const generateIframeUrl = () => {
  gistUrl.value = `${props.data.url}.pibb`
}

watch(
  () => props.data.url,
  async () => {
    generateIframeUrl()
    await checkGistLoaded()
  },
  { immediate: true }
)
</script>

